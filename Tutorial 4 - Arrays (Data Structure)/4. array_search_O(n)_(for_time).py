#Printing all elements (basically Searching/Scanning the whole data structure) is Big O Notation 
#of Time Complexity of O(n)

#This is because to do so, the algorithm needs to go to every element in the Array Data Structure hence
#it scales with n number of elements in the Array order to print it out every single element hence
#printing/searching/scanning is Big O Notation of Time Complexity of O(n) for Arrays.

stock_price = [298,305,320,301,292]
for i in stock_price:
    print(i)